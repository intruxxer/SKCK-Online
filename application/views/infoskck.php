		<p style="text-align:justify"><strong>Ketentuan pengambilan SKCK:</strong></p>
		<blockquote>
			<ul>
				<li style="text-align:justify">Pemohon SKCK yang melakukan registrasi <em>online via e-SKCK</em> sebelum pukul 08:00 waktu setempat dapat mengambil SKCK di loket pelayanan sampai dengan pukul 14:00 pada hari yang sama dengan membawa slip tanda terima yang menunjukkan kode No. registrasi serta dokumen yang dipersyaratkan kepada petugas loket pelayanan,</li>
				<li style="text-align:justify">Pemohon SKCK yang telah melakukan registrasi <em>online</em> diberikan kesempatan untuk mengambil SKCK paling lama 3 (tiga) hari kerja. Bila melebihi waktu tersebut, sistem akan otomatis menghapus data pemohon dan pemohon harus melakukan registrasi ulang.</li>
			</ul>
