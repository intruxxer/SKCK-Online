    <div role="tabpanel" class="tab-pane" id="cara">
    	<p style="text-align:justify"></p>
    	<p style="text-align:justify"><strong>Tata cara permohonan untuk memperoleh SKCK dapat dilakukan dengan cara:</strong></p>
	    <blockquote>
	    	<ul>
				<li style="text-align:justify"><b>Manual:</b> mendaftar secara langsung di loket pelayanan SKCK yang berada di kantor Polsek/Polres/Polda/Markas Besar dengan membawa dokumen yang dipersyaratkan serta mengisi formulir yang telah disiapkan oleh petugas; atau</li>
				<li style="text-align:justify"><b>Online:</b> mendaftar permohonan SKCK secara <b>online via e-SKCK</b> ini dengan cara mengisi form yang tersedia sesuai dengan urutan.</li>
			</ul>
		</blockquote>
    </div>
    <div role="tabpanel" class="tab-pane" id="syarat">
    	<p style="text-align:justify"></p>
	    <p style="text-align:justify"><strong>Dokumen yang dipersyaratkan untuk permohonan SKCK (diunggah online) melalui<em> e-SKCK</em> adalah sebagai berikut:</strong></p>
		<blockquote>
			<ul>
				<li style="text-align:justify"><em>Copy Scan</em> <b>KTP</b> Asli;</li>
				<li style="text-align:justify"><em>Copy Scan</em> <b>Kartu Keluarga (KK)</b> asli;</li>
				<li style="text-align:justify"><em>Copy Scan</em> <b>Akte Kenal Lahir</b> Asli;</li>
				<li style="text-align:justify"><em>Copy Scan</em> Surat Pengantar dari <b>Desa/Kelurahan</b>;</li>
				<li style="text-align:justify"><em>Copy Scan</em> Surat Pengantar dari <b>Kecamatan</b>;</li>
				<li style="text-align:justify"><em>Copy Scan</em> Surat Pengantar dari <b>Polsek</b>;</li>
				<li style="text-align:justify"><em>Copy Scan</em> <b>Identitas lain</b> bagi pemohon yang <u>belum</u> memenuhi syarat memperoleh KTP;</li>
				<li style="text-align:justify"><em>Copy Scan</em> <b>Foto diri ukuran 4 x 6</b> (bukan foto selfie) berwarna latar belakang merah, berpakaian sopan, tampak muka. Bagi pemohon yang mengenakan jilbab harus tampak muka;</li>
				<li style="text-align:justify"><em>Copy Scan</em> <b>Paspor,</b> bagi WNI yang akan keluar negeri dalam rangka sekolah/kunjungan/penerbitan VISA;</li>
			</ul>
		<p style="margin-left:20px; text-align:justify"></p>
		<p style="margin-left:20px; text-align:justify">Pada saat pengambilan SKCK di loket pelayanan, pemohon <b>WAJIB</b> menunjukkan dokumen asli yang dipersyaratkan di atas kepada petugas loket guna keperluan verifikasi.</p>
		<p style="margin-left:20px; text-align:justify">Jangan lupa, bawalah foto 4 X 6 (bukan foto selfie, berpakaian sopan, tampak muka, dan background warna merah) sebanyak <b>6 lembar</b> pada saat pengambilan SKCK anda.</p> 
    	</blockquote>
